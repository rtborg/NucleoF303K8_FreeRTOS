/**
 * Send a message via the UART
 */
void vTask1_handler(void *params) {
	while (1) {
		HAL_UART_Transmit(&huart2, buffer, sizeof(buffer), 100);
		vTaskDelay(1000);
	}
}

/**
 * Blink on-board LED
 */
void vTask2_handler(void *params) {
	while (1) {
		HAL_GPIO_TogglePin(LD3_GPIO_Port, LD3_Pin);
		vTaskDelay(500);
	}
}

